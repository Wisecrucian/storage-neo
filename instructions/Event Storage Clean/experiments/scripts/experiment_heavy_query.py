#!/usr/bin/env python3
"""
Адаптация прод-OLAP к events_raw + 10× idle / 10× под записью.
Результат: EXPERIMENTS_HEAVY_QUERY.md (генерируется ниже).
"""
from __future__ import annotations

import importlib.util
import json
import statistics
import sys
import threading
import time
import uuid
import urllib.parse
import urllib.request
from pathlib import Path

ROOT = Path(__file__).resolve().parent


def _load_exp2():
    spec = importlib.util.spec_from_file_location("experiments_phase2", ROOT / "experiments_phase2.py")
    m = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(m)
    return m


exp2 = _load_exp2()
CH = exp2.CH


def percentile(xs: list[float], p: float) -> float:
    if not xs:
        return 0.0
    xs = sorted(xs)
    k = min(max(int(len(xs) * p / 100), 0), len(xs) - 1)
    return round(xs[k], 2)


def summ_ms(ms: list[float]) -> dict:
    return {
        "avg_ms": round(statistics.mean(ms), 2) if ms else 0,
        "p50_ms": percentile(ms, 50),
        "p95_ms": percentile(ms, 95),
    }


def ch_qid(q: str, query_id: str, timeout: float = 600) -> None:
    qs = urllib.parse.urlencode({"query_id": query_id})
    url = f"{CH}/?{qs}"
    req = urllib.request.Request(url, data=q.encode(), method="POST")
    with urllib.request.urlopen(req, timeout=timeout) as r:
        r.read()


def ch_plain(q: str, timeout: float = 300) -> str:
    req = urllib.request.Request(CH + "/", data=q.encode(), method="POST")
    with urllib.request.urlopen(req, timeout=timeout) as r:
        return r.read().decode()


def query_log_row(query_id: str, retries: int = 25) -> dict | None:
    esc = query_id.replace("'", "''")
    q = (
        "SELECT read_rows, read_bytes, query_duration_ms, result_rows, written_rows "
        f"FROM system.query_log WHERE query_id = '{esc}' AND type = 'QueryFinish' "
        "ORDER BY event_time DESC LIMIT 1 FORMAT JSONEachRow"
    )
    for _ in range(retries):
        try:
            line = ch_plain(q, timeout=60).strip()
            if line:
                row = json.loads(line.split("\n")[0])
                if int(row.get("query_duration_ms", 0) or 0) > 0:
                    return row
        except Exception:
            pass
        time.sleep(0.15)
    return None


# Адаптация URL-параметров к default.events_raw (см. architecture.md).
# minutes=0 → в прототипе фиксированное окно 90d (иначе неограниченный скан).
#
# Schema-aware mapping (storage_schema_sim.py):
#   UserActivityType.REGISTRATION → RecordType.SYSTEM  (attrs['type'] = 'REGISTRATION')
#   attrs keys: terminal_type, user_active_status ("1"/"0"), gender, user_device_type, org_hash
#   All keys are generated by SchemaAwareRecordGenerator for REGISTRATION events.
HEAVY_SQL_BODY = """
WITH filtered AS (
  SELECT user_id, event_time
  FROM default.events_raw
  WHERE record_type = 'SYSTEM'
    AND attrs['type'] = 'REGISTRATION'
    AND attrs['terminal_type'] = 'ONEME'
    AND attrs['user_active_status'] = '1'
    AND attrs['gender'] = '0'
    AND attrs['user_device_type'] = 'ANDROID'
    AND attrs['org_hash'] IN (
      '-7207924629501764854',
      '-4231934642844376041',
      '1136490941434531770',
      '4369003663347448391'
    )
    AND event_time >= now() - INTERVAL 90 DAY
),
top_users AS (
  SELECT user_id
  FROM filtered
  GROUP BY user_id
  ORDER BY count() DESC
  LIMIT 50
)
SELECT
  f.user_id,
  toStartOfHour(f.event_time) AS ts,
  count() AS events
FROM filtered AS f
WHERE f.user_id IN (SELECT user_id FROM top_users)
GROUP BY f.user_id, ts
ORDER BY events DESC
LIMIT 5000
""".strip()

HEAVY_RUN = HEAVY_SQL_BODY + "\nFORMAT Null"


def run_ten(
    prefix: str,
) -> tuple[list[float], list[dict], list[int | None], list[int | None]]:
    lat_ms: list[float] = []
    logs: list[dict] = []
    read_rows_l: list[int | None] = []
    read_bytes_l: list[int | None] = []
    for i in range(10):
        qid = f"{prefix}_{i:02d}_{uuid.uuid4().hex}"
        t0 = time.time()
        ch_qid(HEAVY_RUN, qid, timeout=600)
        ms = (time.time() - t0) * 1000
        lat_ms.append(ms)
        try:
            ch_plain("SYSTEM FLUSH LOGS", timeout=30)
        except Exception:
            pass
        row = query_log_row(qid, retries=8)
        logs.append(row or {})
        if row:
            read_rows_l.append(int(row.get("read_rows", 0)))
            read_bytes_l.append(int(row.get("read_bytes", 0)))
        else:
            read_rows_l.append(None)
            read_bytes_l.append(None)
    return lat_ms, logs, read_rows_l, read_bytes_l


def explain_text() -> str:
    return ch_plain("EXPLAIN indexes=1\n" + HEAVY_SQL_BODY + "\nFORMAT TSVRaw", timeout=120)


def fenced(txt: str, lim: int = 14000) -> str:
    body = txt if len(txt) <= lim else txt[:lim] + "\n… [обрезано для MD]"
    return f"```\n{body}\n```\n"


def infra_ok() -> bool:
    try:
        with urllib.request.urlopen(CH + "/ping", timeout=3) as r:
            if b"Ok" not in r.read():
                return False
    except Exception:
        return False
    try:
        urllib.request.urlopen(exp2.APP + "/actuator/health", timeout=3)
    except Exception:
        return False
    return True



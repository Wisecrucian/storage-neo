def main() -> None:
    if not infra_ok():
        write_placeholder_md("ClickHouse или Java API недоступны (connection refused).")
        print("Wrote EXPERIMENTS_HEAVY_QUERY.md (placeholder). Поднимите CH + API и перезапустите.", file=sys.stderr)
        sys.exit(1)

    exp2.preflight()

    explain_idle = explain_text()

    print("10× heavy query (idle)…")
    idle_lat, idle_logs, idle_rr, idle_rb = run_ten("heavy_idle")

    print("Starting 4 writers (batch=2000, sleep=0)…")
    exp2.stop_writers.clear()
    with exp2.write_lock:
        exp2.write_ok = 0
        exp2.write_err = 0
    writers = [
        threading.Thread(target=exp2.writer_thread_throttled, args=(0.0,), daemon=True)
        for _ in range(4)
    ]
    for w in writers:
        w.start()
    time.sleep(3)

    explain_load = explain_text()

    print("10× heavy query (under load)…")
    load_lat, load_logs, load_rr, load_rb = run_ten("heavy_load")

    exp2.stop_writers.set()
    for w in writers:
        w.join(timeout=60)

    with exp2.write_lock:
        w_ok, w_err = exp2.write_ok, exp2.write_err

    md = []
    md.append("# EXPERIMENTS: тяжёлый OLAP (адаптация прод-запроса → `events_raw`)\n")
    md.append(f"_Сгенерировано: {time.strftime('%Y-%m-%dT%H:%M:%SZ', time.gmtime())}_\n")
    md.append("## Исходные параметры (прод)\n")
    md.append(
        "- `topBy=USER_ID`, `chunkSize=50` → CTE `top_users`: топ-50 `user_id` по числу событий после фильтров.\n"
        "- `chartBy=TIMESTAMP` → внешняя агрегация: `toStartOfHour(event_time)`.\n"
        "- `types=REGISTRATION` → `record_type = 'SYSTEM'` + `attrs['type'] = 'REGISTRATION'`"
        " (schema_sim: REGISTRATION → RecordType.SYSTEM).\n"
        "- `terminalTypes=ONEME` → `attrs['terminal_type'] = 'ONEME'`.\n"
        "- `userActiveStatus=1` → `attrs['user_active_status'] = '1'`.\n"
        "- `queryString`: ORG_HASH (4 значения), GENDER=0, USER_DEVICE_TYPE=ANDROID → см. SQL.\n"
        "- `minutes=0` → в песочнице: **`event_time >= now() - INTERVAL 90 DAY`** (ограничение скана; в проде уточнить семантику «0»).\n"
    )
    md.append("## SQL (как выполнялся)\n")
    md.append("```sql\n" + HEAVY_RUN + "\n```\n")
    md.append("## Методика\n")
    md.append(
        "- **Idle:** 10 последовательных прогонов; после каждого — `SYSTEM FLUSH LOGS` и чтение метрик из `system.query_log` по уникальному `query_id`.\n"
        "- **Нагрузка:** 4 потока `save-batch`, batch **2000**, sleep **0** (как в phase2), прогрев **3 с**, затем 10 прогонов того же запроса.\n"
        "- **Латентность:** время до завершения HTTP-запроса (клиент), плюс `query_duration_ms` из лога (сервер) — ниже.\n"
        "- **read_rows / read_bytes:** из `system.query_log` (`type = 'QueryFinish'`) по `query_id`.\n"
    )
    md.append("## Результаты: латентность (клиент, мс)\n")
    md.append("| Фаза | avg | p50 | p95 |\n")
    md.append("|---|---:|---:|---:|\n")
    id_s = summ_ms(idle_lat)
    ld_s = summ_ms(load_lat)
    md.append(
        f"| Idle | {id_s['avg_ms']} | {id_s['p50_ms']} | {id_s['p95_ms']} |\n"
        f"| Под записью | {ld_s['avg_ms']} | {ld_s['p50_ms']} | {ld_s['p95_ms']} |\n"
    )
    md.append("## Результаты: `query_duration_ms` из `system.query_log` (мс)\n")

    def qdur_stats(logs: list[dict]) -> dict:
        ms = [
            float(r["query_duration_ms"])
            for r in logs
            if r.get("query_duration_ms") is not None and int(r["query_duration_ms"]) > 0
        ]
        return summ_ms(ms) if ms else {"avg_ms": 0, "p50_ms": 0, "p95_ms": 0}

    id_q = qdur_stats(idle_logs)
    ld_q = qdur_stats(load_logs)
    md.append("| Фаза | avg | p50 | p95 |\n")
    md.append("|---|---:|---:|---:|\n")
    md.append(
        f"| Idle | {id_q['avg_ms']} | {id_q['p50_ms']} | {id_q['p95_ms']} |\n"
        f"| Под записью | {ld_q['avg_ms']} | {ld_q['p50_ms']} | {ld_q['p95_ms']} |\n"
    )
    md.append("## Результаты: read_rows / read_bytes (`system.query_log`)\n")
    md.append("| Фаза | read_rows (avg) | read_bytes (avg) |\n")
    md.append("|---|---:|---:|\n")

    def avg_int(xs: list[int | None]) -> str:
        v = [x for x in xs if x is not None]
        if not v:
            return "n/a"
        return str(int(round(statistics.mean(v))))

    md.append(f"| Idle | {avg_int(idle_rr)} | {avg_int(idle_rb)} |\n")
    md.append(f"| Под записью | {avg_int(load_rr)} | {avg_int(load_rb)} |\n")
    md.append(
        f"\nЗапись за окно нагрузки: **{w_ok}** строк, ошибок батчей: **{w_err}**.\n\n"
    )

    md.append("## Приложение: прогоны по штучно\n\n")
    md.append("### Idle\n\n")
    md.append("| # | client_ms | read_rows | read_bytes | query_duration_ms |\n")
    md.append("|---:|---:|---:|---:|---:|\n")
    for i in range(10):
        lg = idle_logs[i] if i < len(idle_logs) else {}
        md.append(
            f"| {i + 1} | {round(idle_lat[i], 2) if i < len(idle_lat) else '—'} | "
            f"{idle_rr[i] if i < len(idle_rr) and idle_rr[i] is not None else 'n/a'} | "
            f"{idle_rb[i] if i < len(idle_rb) and idle_rb[i] is not None else 'n/a'} | "
            f"{lg.get('query_duration_ms', 'n/a')} |\n"
        )
    md.append("\n### Под записью\n\n")
    md.append("| # | client_ms | read_rows | read_bytes | query_duration_ms |\n")
    md.append("|---:|---:|---:|---:|---:|\n")
    for i in range(10):
        lg = load_logs[i] if i < len(load_logs) else {}
        md.append(
            f"| {i + 1} | {round(load_lat[i], 2) if i < len(load_lat) else '—'} | "
            f"{load_rr[i] if i < len(load_rr) and load_rr[i] is not None else 'n/a'} | "
            f"{load_rb[i] if i < len(load_rb) and load_rb[i] is not None else 'n/a'} | "
            f"{lg.get('query_duration_ms', 'n/a')} |\n"
        )
    md.append("\n")

    md.append("## EXPLAIN indexes=1\n")
    md.append("### Idle (до нагрузочных прогонов)\n")
    md.append(fenced(explain_idle))
    md.append("### Под нагрузкой записи (~3 с после старта 4 потоков)\n")
    md.append(fenced(explain_load))
    md.append("## Заметки\n")
    md.append(
        "- Schema-aware генератор (`storage_schema_sim.py`) гарантирует присутствие ключей"
        " `attrs['org_hash']`, `terminal_type`, `user_active_status`, `gender`, `user_device_type`"
        " во всех событиях типа `REGISTRATION` (record_type='SYSTEM')."
        " Результаты запроса ненулевые при достаточном объёме данных (~1/30 записей — REGISTRATION).\n"
        "- Для сопоставления с продом имеет смысл повторить эксперимент на дампе/реплике с реальным распределением `attrs`.\n"
    )

    out = ROOT / "EXPERIMENTS_HEAVY_QUERY.md"
    out.write_text("".join(md), encoding="utf-8")
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
